#Dados do tipo booleano podem assumir dois valores (True ou False)
#Exemplo
status_Aprovacao = True;

print("O aluno foi aprovado?")
print(status_Aprovacao)

#Identificando o tipo de dado
print(type(status_Aprovacao))

#Negação: Para negar o valor de um booleano, basta acrescentaqr o (not) antes da variável

print("O aluno foi aprovado?")
print(not status_Aprovacao)